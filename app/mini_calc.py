class MiniCalc:
    def __init__(self, a, b):
        self.a = a
        self.b = b

    def adunare(self):
        pass
        #return self.a + self.b
        return sum([self.a, self.b])

    def scadere(self):
        pass
        return self.a - self.b

    def inmultire(self):
        pass
        return self.a * self.b

    def impartire(self):
        pass
        return self.a / self.b